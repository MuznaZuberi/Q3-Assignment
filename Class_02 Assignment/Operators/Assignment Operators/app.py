# Assignment Operators


#  = 
a : int = 12;
print("The result is:" , a); #Output 12



# +=
b :int = 19;
b +=3
print("The result is:" , b); #Output 22



# -=
b :int = 19;
b -=10
print("The result is:" , b); #Output 9


# *=
b :int = 9;
b *=4
print("The result is:" , b); #Output 36




# /=
b : int = 20;
b /=4
print("The result is:" , b);	#Output 5.0





# %=
b : int = 20;
b %=4
print("The result is:" , b);  #Output 0

