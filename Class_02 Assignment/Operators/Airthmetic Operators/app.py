#  Arithmetic Operators


# Addition +
a : int = 200;
b : int = 233;
x = a+b;
print("Addition:" , x);



# Subtraction - 
a : int = 20;
b : int = 14;
y = a-b;
print("Subtraction:" , y);



# Multiplication *
a : int = 19;
b : int = 8;
z = a*b;
print("Multiplication:" , z);


# Division  /
a : int = 1000;
b : int = 5;
m = a/b;
print("Division:" , m);



# Modulus (remainder) %	
a : int = 10;
b : int = 3;
n = a%b;
print("Modulus:" , n);


# Exponentiation **
k : int = 4**3;
print("Exponentiation:" , k);



# Floor Division //

c : float = 28.7;
d : int = 3;
res = c//d;
print("Floor Division:", res);